static void each_op(_result_t(*unit_test)(CLOCK_T* outAlgorithmElapsedTime, _operationdescriptor* operation, void* userData), int boolRepeat,
	_char_t const* const test_description, void* userData, unsigned int numberOfRepeat, double operand_size1, double operand_size2
)
{
	_operationdescriptor* descriptor = arithmetic->multiply;
	int i;
	for(i = 0; i < arithmetic->multiplycount; ++i)
	{
		_rand_seed_3 = _R(0x4d595df4d0f33173); /*deterministic , i want all tests to be reproducible*/
		srand((unsigned int)_rand_seed_3);

		if (boolRepeat)
		{
			run_test_repeat(unit_test, descriptor, test_description, userData, numberOfRepeat, operand_size1, operand_size2);
		}
		else
		{
			run_test_single(unit_test, descriptor, test_description, userData, operand_size1, operand_size2);
		}

		descriptor++;
	}
}

static _result_t multiply_do_not_mess_with_memory(CLOCK_T* delta, _operationdescriptor* impl, void* userData)
{
	UNUSED(userData);
	_result_t result = _OK;
	numsize_t ASize;
	numsize_t BSize;
	numsize_t RSize;
	numsize_t ExpectedSize;

	_char_t* A = STR("B7EFC6DD66A58A99C1DB63650EDB21719287D311C2E9E70B5C7913FB41B0433F175C3194A98B0C0D6DB24FAFCA5A959641CA43FFC811A55A33A4E06C5C6BAE8DC36510CE2C13AC6A9D36932A38420AA8E2CFE71BE95A2FDB78F2A6B33F502D02");
	_char_t* B = STR("8BE5E79AE823FC2D8642D85487303540D133DF7C37C7E523140CC9731352B38AB68CF110707B174F90CA96F97894C76C92E5BF803916D55AEB2C6FCEF82739C93FEDC8B0B16DA5BC1576AD35130885112E1F61AEAA33A2A7D5891B29961C7716");
	_char_t* C = STR("648460DFC44344572A1D242EDBC78CE049492455B5D304A920A30383FAC2C1B39C30CC58247A0B5EBE4BC90DD3F217F1B7DD1C89877361FCAA342EED7CFAA627276BC351B6B2D8083F31BABEB43A1EBCC5E913CC76A84B9228AADA00D3E3A63F97ED6C0E207960E4797B9BA268763B6877D245DF9C2F81F5EC5C333F219C97D2FBEA375CCB846095F127E28EBBDEB8FB345FBF278C04B0D1201E1B3441485D1102BC6EBA32B08E39D49188C957DD94C3C8FB037474195C57CBB3F84BCE07CC2C");


	reg_t* _A = _HALF_MEG_mA + 10;
	reg_t* _B = _HALF_MEG_mB + 10;
	reg_t* _C = _HALF_MEG_mC + 10;
	reg_t* _R = _MEG_mRESULT1 + 10;

	const reg_t magic = _R(-5012312);

	for (int i = -10; i < 0 ; ++i)
		_A[i] = _B[i] = _C[i] = _R[i] = magic;


	parseFromHex(A
		, _A, HALF_MEG_NUMBER, &ASize);

	parseFromHex(B
		, _B, HALF_MEG_NUMBER, &BSize);

	parseFromHex(C
		, _C, HALF_MEG_NUMBER, &ExpectedSize);

	for (int i = 0; i < 10; ++i)
	{
		_A[ASize +i] = magic;
		_B[BSize +i] = magic; 
		_R[ExpectedSize+i ] = magic;
	}
	*delta = 0;
	RSize = impl->operation.operation(_A, ASize, _B, BSize, _R);

	if (_A[-1] != magic || _A[ASize ] != magic)
	{
		LOG_ERROR(STR("A digit which should not have been touched has been modified in A"));
		result = _FAIL;
	}
	if (_B[-1] != magic || _B[BSize ] != magic)
	{
		LOG_ERROR(STR("A digit which should not have been touched has been modified in B"));
		result = _FAIL;
	}
	if (_R[-1] != magic || _R[RSize ] != magic)
	{
		LOG_ERROR(STR("A digit which should not have been touched has been modified in R"));
		result = _FAIL;
	}

	return result;
}

